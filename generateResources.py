def dust_craft_creation():
    smalldust_crafts = RECIPES.joinpath("smalldust")
    if not os.path.exists(smalldust_crafts):
        os.makedirs(smalldust_crafts, True)
    for input_name, metadata in SMALL_DUSTS.items():
        small_to_dust = smalldust_crafts.joinpath(f"{input_name}_dust.json")
        if not os.path.exists(small_to_dust):
            output_metadata = DUSTS.get(input_name)
            with open(small_to_dust, "w") as file:
                if output_metadata is None:
                    if input_name == "glowstone":
                        input_name = "glowstone_dust"
                    file.write(
                        two_by_two_metadata_craft(
                            MODID + ":smalldust", "minecraft:" + input_name, metadata
                        )
                    )
                else:
                    file.write(
                        two_by_two_metadata_craft(
                            MODID + ":smalldust",
                            MODID + ":dust",
                            metadata,
                            output_metadata,
                            )
                    )
    dust_crafts = RECIPES.joinpath("dust")
    if not os.path.exists(dust_crafts):
        os.makedirs(dust_crafts, True)
    for input_name, metadata in DUSTS.items():
        dust_to_small = dust_crafts.joinpath(f"{input_name}_smalldust.json")
        if not os.path.exists(dust_to_small):
            output_metadata = SMALL_DUSTS.get(input_name)
            with open(dust_to_small, "w") as file:
                file.write(
                    one_to_out_metadata_craft(
                        MODID + ":dust",
                        MODID + ":smalldust",
                        metadata,
                        output_metadata,
                        4
                    )
                )
    with open(dust_crafts.joinpath("glowstone_smalldust.json"), "w") as file:
        output_metadata = SMALL_DUSTS.get("glowstone")
        file.write(
            one_to_out_metadata_craft(
                "minecraft:glowstone_dust",
                MODID + ":smalldust",
                0,
                output_metadata,
                4
            )
        )
    with open(dust_crafts.joinpath("redstone_smalldust.json"), "w") as file:
        output_metadata = SMALL_DUSTS.get("redstone")
        file.write(
            one_to_out_metadata_craft(
                "minecraft:redstone",
                MODID + ":smalldust",
                0,
                output_metadata,
                4
            )
        )


