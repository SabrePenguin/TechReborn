RECIPES = BASE.joinpath("recipes")
ITEM_MODELS = BASE.joinpath("models/item")
ITEM_TEXTURES = BASE.joinpath("textures/items")
BLOCK_MODELS = BASE.joinpath("models/block")
BLOCK_TEXTURES = BASE.joinpath("textures/blocks")


def write_to_file(file_name: str | Path, data: str | None):
    if data is None:
        print(f"Unable to write to file: {file_name}")
        return
    if not os.path.exists(file_name) or OVERWRITE:
        with open(file_name, "w") as file:
            file.write(data)


def image_exists(path) -> bool:
    return os.path.exists(path)


def basic_generated(image) -> str:
    result = {
        "__comment": "Generated by generateResources.py",
        "parent": "item/generated",
        "textures": {"layer0": f"techreborn:{image}"},
    }
    return json.dumps(result, indent=4)


def path_image(path: str, image: str) -> str:
    result = {
        "__comment": "Generated by generateResources.py",
        "parent": "item/generated",
        "textures": {"layer0": f"techreborn:{path}/{image}"},
    }
    return json.dumps(result, indent=4)


def block_all_model(image: str) -> str:
    result = {"parent": "block/cube_all", "textures": {"all": f"techreborn:{image}"}}
    return json.dumps(result, indent=4)


def default_block_model(parent: str) -> str:
    result = {"parent": parent}
    return json.dumps(result, indent=4)


# INGOT creation
def create_ingots():
    ingot_model = ITEM_MODELS.joinpath("ingot")
    ingot_textures = ITEM_TEXTURES.joinpath("ingot")
    if not os.path.exists(ingot_model):
        os.makedirs(ingot_model, True)

    for ingot in registries.INGOTS:
        new_file = ingot_model.joinpath(f"{ingot}_ingot.json")
        if not os.path.exists(ingot_model.joinpath(f"{ingot}_ingot.json")):
            image = str(ingot_textures) + f"/{ingot}_ingot"
            if not image_exists(image + ".png"):
                print(f"Error: image {image}.png doesn't exist")
                continue
            with open(new_file, "w") as file:
                file.write(basic_generated(f"items/ingot/{ingot}_ingot"))


