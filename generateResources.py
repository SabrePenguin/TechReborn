def write_to_file(file_name: str | Path, data: str | None):
    if data is None:
        print(f"Unable to write to file: {file_name}")
        return
    if not os.path.exists(file_name) or OVERWRITE:
        with open(file_name, "w") as file:
            file.write(data)


def image_exists(path) -> bool:
    return os.path.exists(path)


def basic_generated(image) -> str:
    result = {
        "__comment": "Generated by generateResources.py",
        "parent": "item/generated",
        "textures": {"layer0": f"techreborn:{image}"},
    }
    return json.dumps(result, indent=4)


def path_image(path: str, image: str) -> str:
    result = {
        "__comment": "Generated by generateResources.py",
        "parent": "item/generated",
        "textures": {"layer0": f"techreborn:{path}/{image}"},
    }
    return json.dumps(result, indent=4)


def block_all_model(image: str) -> str:
    result = {"parent": "block/cube_all", "textures": {"all": f"techreborn:{image}"}}
    return json.dumps(result, indent=4)


def default_block_model(parent: str) -> str:
    result = {"parent": parent}
    return json.dumps(result, indent=4)

def create_blocks():
    blocks_model = BLOCK_MODELS.joinpath("storage")
    blocks_textures = BLOCK_TEXTURES.joinpath("storage")
    items_model = ITEM_MODELS.joinpath("storage")
    if not os.path.exists(blocks_model):
        os.makedirs(blocks_model, exist_ok=True)
    if not os.path.exists(items_model):
        os.makedirs(items_model, exist_ok=True)

    blocks = [registries.BLOCK_STORAGE, registries.BLOCK_STORAGE2]
    for l in blocks:
        for storage in l.keys():
            fname = blocks_model.joinpath(f"{storage}_block.json")
            image = str(blocks_textures) + f"/{storage}_block"
            if not image_exists(image + ".png"):
                print(f"Error: image {image}.png doesn't exist")
                continue
            write_to_file(fname, block_all_model(f"blocks/storage/{storage}_block"))


def create_tools():
    tool_model = ITEM_MODELS.joinpath("tool")
    tool_textures = ITEM_TEXTURES.joinpath("tool")
    if not os.path.exists(tool_model):
        os.makedirs(tool_model, exist_ok=True)
    for name in registries.TOOLS:
        for t in [
            "axe",
            "boots",
            "chestplate",
            "helmet",
            "hoe",
            "leggings",
            "pickaxe",
            "spade",
            "sword",
        ]:
            fname = tool_model.joinpath(f"{name}{t}.json")
            image = str(tool_textures) + f"/{name}_{t}"
            if not image_exists(image + ".png"):
                print(f"Error: image {image}_{t}.png doesn't exist")
                continue
            write_to_file(fname, basic_generated(f"items/tool/{name}_{t}"))


def create_item_x(directory: str, registry: dict | list, postfix: str = ""):
    model = ITEM_MODELS.joinpath(directory)
    textures = ITEM_TEXTURES.joinpath(directory)
    if not os.path.exists(model):
        os.makedirs(model, exist_ok=True)
    local_keys = []
    if isinstance(registry, list):
        local_keys = registry
    elif isinstance(registry, dict):
        local_keys = registry.keys()
    else:
        return
    for key in local_keys:
        fname = model.joinpath(f"{key}{postfix}.json")
        image = str(textures) + f"/{key}{postfix}"
        if not image_exists(image + ".png"):
            print(f"Error: image {image}{postfix}.png doesn't exist")
            continue
        directory = fix_directory(directory)
        write_to_file(fname, basic_generated(f"items/{directory}{key}{postfix}"))


def fix_directory(directory: str) -> str:
    if directory.endswith("/"):
        return directory
    if directory == "":
        return ""
    return directory + "/"


