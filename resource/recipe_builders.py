class CustomRecipeBuilder(BaseRecipeBuilder):
    def __init__(self, recipe_type: str=""):
        BaseRecipeBuilder.__init__(self)
        self.inputs: list[Ingredient] = []
        self.outputs: list[Ingredient] = []
        self.recipe_time: int = -1
        self.energy_cost: int = -1
        self.experience: float = 0
        self.with_type(recipe_type)

    def with_input(self, ingredient: Ingredient):
        self.inputs.append(ingredient)
        return self

    def with_output(self, ingredient: Ingredient):
        self.outputs.append(ingredient)
        return self

    def with_energy(self, energy: int):
        self.energy_cost = energy
        return self

    def with_time(self, time: int):
        self.recipe_time = time
        return self

    def build(self) -> str | None:
        super().build()

        if len(self.inputs) <= 0:
            raise RuntimeError("Inputs are required")
        if len(self.outputs) == 0:
            raise RuntimeError("There must be an output")

        outputs = [output.to_dict() for output in self.outputs]
        if len(outputs) == 1:
            outputs = outputs[0]

        result: dict[str, str | int | dict | list] = {
            "__comment": "Generated by recipe_builders.py",
            "type": self.recipe_type,
            "ingredients": [i.to_dict() for i in self.inputs],
            "result": outputs,
        }
        if self.recipe_time > 0:
            result["recipe_time"] = self.recipe_time
        if self.energy_cost > 0:
            result["fe_cost"] = self.energy_cost
        if self.experience > 0:
            result["experience"] = self.experience
        return json.dumps(result, indent=4)


class ShapedRecipeBuilder(BaseRecipeBuilder):
    def __init__(self):
        BaseRecipeBuilder.__init__(self)
        self.with_type("minecraft:crafting_shaped")
        self.pattern: list[str] = []
        self.keys: dict[str, Ingredient] = {}
        self.output: list[Ingredient] = []

    def with_pattern(self, pattern: list[str]):
        self.pattern = pattern
        return self

    def with_ingredient(self, key: str, ingredient: Ingredient):
        self.keys[key] = ingredient
        return self

    def with_output(self, output: Ingredient):
        self.output.append(output)
        return self

    def build(self) -> str | None:
        all_keys: set[str] = set([char for row in self.pattern for char in row])
        for key in all_keys:
            if key != " " and key not in self.keys:
                raise RuntimeError(f"Key {key} not in pattern {self.keys}")
        all_keys: dict[str, dict] = dict([(k,v.to_dict()) for k,v in self.keys.items()])
        result: dict[str, str|int|dict|list[dict]] = {
            "__comment": "Generated by recipe_builders.py",
            "type": self.recipe_type,
            "pattern": self.pattern,
            "key": all_keys,
        }
        if len(self.output) == 1:
            result["result"] = self.output[0].to_dict()
        else:
            result["result"] = [out.to_dict() for out in self.output]
        return json.dumps(result, indent=4)


