def validate_item(key: dict) -> bool:
    if not isinstance(key, dict):
        return False
    if key.get("item") is not None:
        return key.get("item")
    elif key.get("fluid") is not None:
        return key.get("fluid") and key.get("count") != 0
    elif key.get("ore") is not None:
        return key.get("type") == "forge:ore_dict" and key.get("ore")
    return False


def validate_items(keys: dict | list) -> bool:
    if isinstance(keys, list):
        for i in keys:
            if not validate_item(i):
                return False
    elif isinstance(keys, dict):
        for _key, i in keys.items():
            if not validate_item(i):
                return False
    else:
        return False
    return True


class Ingredient:
    def __init__(self, name: str, metadata: int = -1, item_type: str = "", count: int = 0, modid: str = "", nbt: dict = None, output: bool = False):
        if nbt is None:
            nbt = {}
        self.item_type: str = item_type
        self.count: int = count
        self.name = name
        self.metadata = metadata
        self.modid = modid
        self.output = output
        self.nbt = nbt
        self.validate()

    def get_name(self):
        return self.name

    @staticmethod
    def meta(registry: dict, item: str, count: int = 0, modid: str="techreborn") -> Ingredient:
        registry_item_base_name = registries.get_name_for_registry(registry)
        data = registry.get(item, -1)
        return Ingredient(registry_item_base_name, metadata=data, count=count, modid=modid)

    @staticmethod
    def item(normal_item: str, metadata: int = -1, count: int = 0, modid: str="minecraft") -> Ingredient:
        return Ingredient(normal_item, metadata=metadata, count=count, modid=modid)

    @staticmethod
    def mod_item(item: str, metadata: int = -1, count: int = 0, modid: str="techreborn") -> Ingredient:
        return Ingredient(item, metadata=metadata, count=count, modid=modid)

    @staticmethod
    def nbt_item(item: str, nbt: str, output: bool, metadata: int = -1, count: int = 0, modid: str="techreborn",) -> Ingredient:
        return Ingredient(item, nbt=nbt, metadata=metadata, count=count, modid=modid, item_type="minecraft:item_nbt", output=output)

    @staticmethod
    def cell(fluid: str, modid: str = "", count: int = 0):
        return Ingredient(fluid, item_type="techreborn:cell", count=count, modid=modid)

    @staticmethod
    def oredict(ore: str, oretype: str = "", count: int = 0):
        if oretype == "":
            return Ingredient(ore, item_type="forge:ore_dict", count=count)
        return Ingredient(oretype + _capitalize_oredict(ore), item_type="forge:ore_dict", count=count)

    @staticmethod
    def fluid(fluid: str, count: int = 1000, modid: str = ""):
        return Ingredient(fluid, item_type="techreborn:fluid_container", count=count, modid=modid)

    def validate(self):
        if self.item_type == "forge:ore_dict":
            if self.name == "":
                raise RuntimeError("Ore name is empty")
        elif self.item_type == "techreborn:fluid_container":
            if self.name == "":
                raise RuntimeError("Fluid ingredient name is empty")
        elif self.item_type == "techreborn:cell":
            if self.name == "":
                raise RuntimeError("No fluid in cell, did you mean to make an empty cell instead (Ingredient.mod_item)?")
        else:
            if self.name == "":
                raise RuntimeError("Ingredient name is empty")
            if self.modid == "":
                raise RuntimeError("Ingredient " + self.name + " must have a modid")

    def to_dict(self) -> dict:
        if self.item_type == "forge:ore_dict":
            result: dict[str, str|int] = {
                "type": self.item_type,
                "ore": self.name
            }
            if self.count > 0:
                result["count"] = self.count
            return result
        elif self.item_type == "techreborn:fluid_container":
            fluid = self.name
            if self.modid != "":
                fluid = self.modid + ":" + self.name
            return {
                "type": self.item_type,
                "fluid":  fluid,
                "amount": self.count
            }
        elif self.item_type == "techreborn:cell":
            fluid = self.name
            if self.modid != "":
                fluid = self.modid + ":" + self.name
            return {
                "type": self.item_type,
                "fluid": fluid,
            }
        else:
            result: dict[str, str|int] = {}
            if self.item_type == "minecraft:item_nbt" and not self.output:
                result["type"] = self.item_type
            result["item"] = self.modid + ":" + self.name
            if self.metadata >= 0:
                result["data"] = self.metadata
            if self.count > 0:
                result["count"] = self.count
            if self.item_type == "minecraft:item_nbt" and isinstance(self.nbt, dict):
                result["nbt"] = self.nbt
            return result

    def to_json(self) -> str:
        return json.dumps(self.to_dict(), indent=4)

def _capitalize_oredict(ore_in: str) -> str:
    words: list[str] = ore_in.split("_")
    out = ""
    for word in words:
        out = out + word.capitalize()
    return out

class RecipeMultiplier:
    def __init__(self, name, recipe_builder):
        self.inputs: list[list[Ingredient]] = []
        self.name = name
        if isinstance(recipe_builder, ShapedRecipeBuilder):
            raise RuntimeError("Ore Dict Multiplier does not implement ShapedRecipeBuilder helper")
        if isinstance(recipe_builder, BaseRecipeBuilder):
            self.recipe = recipe_builder
        else:
            raise RuntimeError("Recipe builder does not inherit BaseRecipeBuilder!")

    def add_inputs(self, ingredient: list[Ingredient]):
        self.inputs.append(ingredient)
        return self

    def run_build(self) -> dict:
        output = {}
        for combination in itertools.product(*self.inputs):
            if isinstance(self.recipe, CustomRecipeBuilder):
                self.recipe.inputs = []
                new_name = self.name + "_"
                ingredient: Ingredient
                for ingredient in combination:
                    self.recipe.with_input(ingredient)
                    new_name += ingredient.get_name()[0]
                output[new_name] = self.recipe.build()
            else:
                raise RuntimeError("Not yet implemented on this object")
        return output